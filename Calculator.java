public class Calculator{

  public static int sum(int a, int b) {
    return a + b;
  }
  public static int minus(int a, int b) {
    return a - b;
  }
  public static int multiplication(int a, int b) {
    return a * b; // comment
  }
  public static int divide(int a, int b){
    return a / b;
  }
}
